public class Main3 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        int num = 10;
        while (num > 0) {
            System.out.println(num);
            num--;
        }

        num = 10;
        do {
            System.out.println(num);
            num--;
        } while (num > 0);
    }
}
